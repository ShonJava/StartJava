package com.startjava.lesson_1.variable;

public class Variable {
    public static void main(String[] args) {
        boolean notebook = true;
        byte core = 2;
        short ram = 4096;
        int pixels4kscreen = 8294400;
        long cpuFrequencyHz = 2000000000;
        char model = 'R';
        float pi = 3.14f;
        double fantasticNumber = 1234.5677890d;
        System.out.println("notebook? " + notebook);
        System.out.println("core? " + core);
        System.out.println("ram? " + ram);
        System.out.println("pixels4kscreen? " + pixels4kscreen);
        System.out.println("cpuFrequencyHz? " + cpuFrequencyHz);
        System.out.println("model? " + model);
        System.out.println("pi? " + pi);
        System.out.println("fantasticNumber? " + fantasticNumber);
    }
}